def show_application_tracker_page(username):
    """Display job application tracking dashboard"""
    st.subheader("💼 Application Tracker")

    # Get saved jobs with different statuses
    saved_jobs = get_saved_jobs(username)
    applied_jobs = [job for job in saved_jobs if job.get('applied', 0) == 1]
    interview_jobs = [job for job in saved_jobs if job.get('status', '').lower() == 'interview']

    # Stats cards
    col1, col2, col3 = st.columns(3)
    col1.metric("Saved Jobs", len(saved_jobs))
    col2.metric("Applications", len(applied_jobs))
    col3.metric("Interviews", len(interview_jobs))

    # Application timeline
    if applied_jobs:
        st.subheader("Application Timeline")

        # Create timeline data
        timeline_data = []
        for job in applied_jobs:
            if job.get('application_date'):
                timeline_data.append({
                    'date': job['application_date'],
                    'title': job.get('Job Title', job.get('job_title', 'Unknown')),
                    'company': job.get('Company', job.get('employer_name', 'Unknown')),
                    'type': 'application'
                })
            if job.get('interview_date'):
                timeline_data.append({
                    'date': job['interview_date'],
                    'title': job.get('Job Title', job.get('job_title', 'Unknown')),
                    'company': job.get('Company', job.get('employer_name', 'Unknown')),
                    'type': 'interview'
                })

        # Sort by date
        timeline_data.sort(key=lambda x: x['date'])

        # Display timeline
        for event in timeline_data[-5:]:  # Show last 5 events
            icon = "📅" if event['type'] == 'application' else "🤝"
            st.write(f"{icon} **{event['date']}**: {event['title']} at {event['company']}")

    # Update job status
    st.subheader("Update Application Status")

    if saved_jobs:
        job_titles = [f"{job.get('Job Title', job.get('job_title', 'Unknown'))} - {job.get('Company', job.get('employer_name', 'Unknown'))}"
                     for job in saved_jobs]
        selected_job = st.selectbox("Select a job", job_titles)

        if selected_job:
            selected_index = job_titles.index(selected_job)
            job_id = saved_jobs[selected_index]['job_id']

            col1, col2 = st.columns(2)
            with col1:
                new_status = st.selectbox("Status", ["Saved", "Applied", "Interview", "Rejected", "Offer"])
            with col2:
                if new_status == "Applied":
                    application_date = st.date_input("Application Date")
                elif new_status == "Interview":
                    interview_date = st.date_input("Interview Date")
                else:
                    st.write("")  # Spacer

            if st.button("Update Status"):
                if new_status == "Applied":
                    update_job_status(username, job_id, new_status, application_date.strftime('%Y-%m-%d'))
                elif new_status == "Interview":
                    update_job_status(username, job_id, new_status, None, interview_date.strftime('%Y-%m-%d'))
                else:
                    update_job_status(username, job_id, new_status)

                st.success("Status updated successfully!")
                st.rerun()
    else:
        st.info("You haven't saved any jobs yet")

