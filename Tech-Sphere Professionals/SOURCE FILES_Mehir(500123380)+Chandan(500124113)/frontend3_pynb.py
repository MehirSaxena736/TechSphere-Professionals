def analyze_skill_gaps(username, job_skills):
    """Analyze skill gaps between user and job requirements"""
    conn = get_db_connection()
    try:
        user = conn.execute('SELECT skills FROM users WHERE username = ?', (username,)).fetchone()
        if not user or not user['skills']:
            return None

        user_skills = set([skill.strip().lower() for skill in user['skills'].split(',')])
        required_skills = set([skill.strip().lower() for skill in job_skills.split(',')])

        missing_skills = required_skills - user_skills

        if missing_skills:
            # Get learning resources for missing skills
            resources = []
            for skill in missing_skills:
                resources.extend(learning_resources.get_resources(skill))

            # Store analysis
            conn.execute(
                'INSERT INTO skill_gaps (username, missing_skills, recommended_resources) VALUES (?, ?, ?)',
                (username, ', '.join(missing_skills), json.dumps(resources))
            )
            conn.commit()

            return {
                'missing_skills': list(missing_skills),
                'resources': resources
            }
        return None
    except Exception as e:
        st.error(f"Error analyzing skill gaps: {str(e)}")
        return None
    finally:
        conn.close()

