class EnhancedJobFinder:
    def __init__(self, file_path="Job opportunities1.csv"):
        """Load job dataset with enhanced processing"""
        try:
            self.df = pd.read_csv(file_path, encoding="ISO-8859-1")
            # Enhanced data cleaning
            self.df['Required Skills'] = self.df['Required Skills'].fillna('').apply(lambda x: x.lower())
            self.df['Location'] = self.df['Location'].fillna('Remote').apply(lambda x: x.title())
            self.df['Company'] = self.df['Company'].fillna('').apply(lambda x: x.title())

            # Extract salary information if available
            if 'Salary Range' in self.df.columns:
                self.df['Min Salary'] = self.df['Salary Range'].apply(self._extract_min_salary)
                self.df['Max Salary'] = self.df['Salary Range'].apply(self._extract_max_salary)

            # Create skill vectors for recommendation
            self._create_skill_vectors()

        except Exception as e:
            st.error(f"Error loading job data: {str(e)}")
            self.df = pd.DataFrame()

    def _extract_min_salary(self, salary_str):
        """Extract minimum salary from salary range string"""
        if pd.isna(salary_str):
            return None
        numbers = re.findall(r'\d+', str(salary_str))
        if numbers:
            return int(numbers[0])
        return None

    def _extract_max_salary(self, salary_str):
        """Extract maximum salary from salary range string"""
        if pd.isna(salary_str):
            return None
        numbers = re.findall(r'\d+', str(salary_str))
        if len(numbers) > 1:
            return int(numbers[1])
        return None

    def _create_skill_vectors(self):
        """Create TF-IDF vectors for skill-based recommendations"""
        if self.df.empty:
            return

        # Create a TF-IDF vectorizer
        self.vectorizer = TfidfVectorizer(stop_words='english', max_features=1000)
        skill_texts = self.df['Required Skills'].fillna('')
        self.skill_vectors = self.vectorizer.fit_transform(skill_texts)

    def filter_jobs(self, Required_Skill="", location="", experience="", company=""):
        """Enhanced job filtering with more parameters"""
        if self.df.empty:
            return None

        filtered_df = self.df.copy()

        if Required_Skill:
            filtered_df = filtered_df[filtered_df['Required Skills'].str.contains(Required_Skill.lower(), case=False, na=False)]
        if location:
            filtered_df = filtered_df[filtered_df['Location'].str.contains(location, case=False, na=False)]
        if company:
            filtered_df = filtered_df[filtered_df['Company'].str.contains(company, case=False, na=False)]

        experience_levels = {
            "entry": ["Entry Level", "0-1 Years", "Intern"],
            "junior": ["Junior", "1-2 Years", "1-3 Years"],
            "mid": ["Mid-Level", "3-5 Years", "2-4 Years", "3-4 Years"],
            "senior": ["Senior", "5-10 Years", "4-6 Years", "5-7 Years"],
            "executive": ["Executive", "Director", "10+ Years", "7+ Years"]
        }

        if experience.lower() in experience_levels:
            exp_categories = experience_levels[experience.lower()]
            filtered_df = filtered_df[filtered_df['Experience Level'].str.contains('|'.join(exp_categories), case=False, na=False)]

        return filtered_df if not filtered_df.empty else None

    def recommend_jobs(self, user_skills, top_n=5):
        """Recommend jobs based on user skills using cosine similarity"""
        if self.df.empty or not hasattr(self, 'vectorizer'):
            return None

        # Transform user skills to vector
        user_skills_text = ' '.join(user_skills).lower()
        user_vector = self.vectorizer.transform([user_skills_text])

        # Calculate similarity
        similarities = cosine_similarity(user_vector, self.skill_vectors).flatten()

        # Get top N recommendations
        top_indices = similarities.argsort()[-top_n:][::-1]
        recommended_jobs = self.df.iloc[top_indices].copy()
        recommended_jobs['Match Score'] = similarities[top_indices]

        return recommended_jobs

    def get_skill_demand(self):
        if self.df.empty:
            return pd.Series()
        return self.df['Required Skills'].str.split(', ').explode().value_counts().head(15)

    def get_location_distribution(self):
        if self.df.empty:
            return pd.Series()
        return self.df['Location'].value_counts().head(10)

    def get_salary_analysis(self):
        if self.df.empty or 'Min Salary' not in self.df.columns:
            return pd.DataFrame()

        salary_df = self.df[['Experience Level', 'Min Salary', 'Max Salary']].dropna()
        if salary_df.empty:
            return pd.DataFrame()

        # Calculate average salary by experience level
        salary_stats = salary_df.groupby('Experience Level').agg({
            'Min Salary': 'mean',
            'Max Salary': 'mean'
        }).round(0)

        return salary_stats

    def get_company_insights(self):
        if self.df.empty:
            return pd.DataFrame()

        company_stats = self.df['Company'].value_counts().head(10)
        return company_stats

class RealTimeJobFetcher:
    def __init__(self, api_key):
        self.api_key = api_key

    def fetch_jobs(self, skill, location="", page=1, num_pages=1):
        try:
            url = "https://jsearch.p.rapidapi.com/search"
            query = f"{skill} developer"
            if location:
                query += f" in {location}"

            querystring = {
                "query": query,
                "page": str(page),
                "num_pages": str(num_pages),
                "date_posted": "month",
                "employment_types": "FULLTIME,PARTTIME,CONTRACTOR"
            }

            headers = {
                "X-RapidAPI-Key": self.api_key,
                "X-RapidAPI-Host": "jsearch.p.rapidapi.com"
            }

            response = requests.get(url, headers=headers, params=querystring, timeout=10)
            response.raise_for_status()

            data = response.json()
            return data.get("data", [])

        except requests.exceptions.RequestException as e:
            st.error(f"Error fetching jobs: {str(e)}")
            return []
        except Exception as e:
            st.error(f"Unexpected error: {str(e)}")
            return []

